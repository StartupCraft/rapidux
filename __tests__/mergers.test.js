import { mergers } from '../src'

describe('Mergers', () => {
  test('latest array merger', () => {
    const result = mergers.latestArrayMerger(['item', 'item'], 'value')

    expect(result).toEqual('value')
  })

  test('latest array merger without array', () => {
    const result = mergers.latestArrayMerger(
      { item: 'item', item2: 'item2' },
      'value',
    )

    expect(result).toEqual(undefined)
  })
})
